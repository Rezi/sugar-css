<hgroup>
	<h1>Typography</h1>
	<h2>Inline elements</h2>
</hgroup>
<article>
	<dl>
		<dt><a href="#">Link</a></dt>
		<dd>
			<code>a</code> HTML element (or anchor element), with its href attribute, creates a hyperlink to
			web pages, files, email addresses, locations in the same page, or anything else a URL can address.
		</dd>

		<dt><abbr title="">Abbreviation</abbr></dt>
		<dd>
			<code>abbr</code> HTML element represents an abbreviation or acronym. The optional
			<code>title</code>
			attribute can provide an explanation for the abbreviation.
		</dd>

		<dt><strong>Strong</strong></dt>
		<dd>
			<code>strong</code> HTML element indicates that its contents have strong importance, seriousness,
			or urgency. Browsers typically render the contents in bold type.
		</dd>

		<dt><strong>Bold</strong></dt>
		<dd>
			<code>b</code> HTML element is used to draw the reader's attention to the element's contents, which
			are not otherwise granted special importance.
		</dd>

		<dt><i>Italic</i></dt>
		<dd>
			<code>i</code> HTML element represents a range of text that is set off from the normal text for
			some reason, such as idiomatic text, technical terms, taxonomical designations, among others.
		</dd>

		<dt><code>Code</code></dt>
		<dd>
			<code>code</code> displays its contents styled in a fashion intended to indicate that the text
			is a short fragment of computer code. By default, the content text is displayed using the user
			agent's default monospace font.
		</dd>

		<dt><i>Emphasis</i></dt>
		<dd>
			<code>em</code> HTML element marks text that has stress emphasis. The element can be nested, with
			each level of nesting indicating a greater degree of emphasis.
		</dd>

		<dt><i>Cite</i></dt>
		<dd>
			<code>cite</code> HTML element is used to mark up the title of a cited creative work. The reference
			may be in an abbreviated form according to context-appropriate conventions related to citation
			metadata.
		</dd>

		<dt><del>Deleted</del></dt>
		<dd>
			<code>del</code> HTML element represents a range of text that has been deleted from a document.
		</dd>

		<dt><ins>Inserted</ins></dt>
		<dd>
			<code>ins</code> HTML element represents a range of text that has been added to a document.
		</dd>

		<dt><kbd>Keyboard&nbsp;key</kbd></dt>
		<dd>
			<code>kbd</code> HTML element represents a span of inline text denoting textual user input from
			a keyboard, voice input, or any other text entry device.
		</dd>

		<dt><mark>Highlight</mark></dt>
		<dd>
			<code>mark</code> HTML element represents text which is marked or highlighted for reference or
			notation purposes due to the marked passage's relevance in the enclosing context.
		</dd>

		<dt><q>Inline quote</q></dt>
		<dd>
			<code>q</code> HTML element indicates that the enclosed text is a short inline quotation.
		</dd>

		<dt><s>Strikethrough</s></dt>
		<dd>
			<code>s</code> HTML element renders text with a strikethrough, or a line through it. Use the element
			to represent things that are no longer relevant or no longer accurate.
		</dd>

		<dt><small>Small</small></dt>
		<dd>
			<code>small</code> HTML element represents side-comments and small print, like copyright and legal
			text, independent of its styled presentation.
		</dd>

		<dt>Text<sub>sub</sub></dt>
		<dd>
			<code>sub</code> HTML element specifies inline text which should be displayed as subscript for
			solely typographical reasons. Subscripts are typically rendered with a lowered baseline using smaller
			text.
		</dd>

		<dt>Text<sup>sup</sup></dt>
		<dd>
			<code>sup</code> HTML element specifies inline text which is to be displayed as superscript for
			solely typographical reasons. Superscripts are usually rendered with a raised baseline using smaller
			text.
		</dd>

		<dt><u>Underline</u></dt>
		<dd>
			<code>sup</code> HTML element specifies inline text which is to be displayed as superscript for
			solely typographical reasons. Superscripts are usually rendered with a raised baseline using smaller
			text.
		</dd>

		<dt><var>Variable</var></dt>
		<dd>
			<code>var</code> HTML element represents the name of a variable in a mathematical expression or
			a programming context.
		</dd>
	</dl>
</article>
